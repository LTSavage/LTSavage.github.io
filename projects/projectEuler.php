                    <h1>Project Euler</h1>
                    <p>As according to its website <a href='https://projecteuler.net/' title='ProjectEuler.net'>projecteuler.net</a>, Project Euler is a series of challenging mathematical/computer programming problems that will require more than just mathematical insights to solve.</p>
                    <h3>Why?</h3>
                    <p>Mathematics is not fulfilling or challenging. This is a massive challenge, a never ending brain teaser</p>
                    <h3>How?</h3>
                    <p>At first I used MATLAB, although I did already know python and Java, because it seemed the most powerful language. However, after further learning I realised that MATLAB is only the easiest, and that Java is the most powerful language I know. I also liked the challenge of applying the same algorithms in python, as some algorithms can be more concise. The real challenge is developing the algorithm, as opposed to applying the algorithms with each language.</p>
